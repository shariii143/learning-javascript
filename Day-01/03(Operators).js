// Arithmetic Operators
let a = 16;
let b = 4;
console.log("Addition:", a + b); // 15
console.log("Subtraction:", a - b); // 5
console.log("Multiplication:", a * b); // 50
console.log("Division:", a / b); // 2
console.log("Modulus:", a % b); // 0

// Comparison Operators
console.log("Equal:", a == b); // false
console.log("Not Equal:", a != b); // true
console.log("Greater Than:", a > b); // true
console.log("Less Than:", a < b); // false
console.log("Greater Than or Equal To:", a >= b); // true
console.log("Less Than or Equal To:", a <= b); // false
console.log("Type check:", a === "10"); // false



// Logical Operators
let isAdult = true;
let hasID = false;
console.log("AND Operator:", isAdult && hasID); // false
console.log("OR Operator:", isAdult || hasID); // true
console.log("NOT Operator:", !isAdult); // false